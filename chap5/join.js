// Create new array
var weather = ["Sunny", "Rainy", "Cloudy", "Drought"];

// Create a string separated by a comma using join() method
console.log(weather.join(", "));

// Create a string separated by double pipes using join() method
console.log(weather.join(" || "));

// Create a string using join() method and save it to a variable
var newString = weather.join(" - ");
console.log(newString);
